package com.frxs.check.model;

import java.io.Serializable;

/**
 * Created by Tiepier
 */
public class GoodsInfo implements Serializable {
    private String ID;//主键编号
    private String ProductId;
    private double Number;
    private int IsCheckRight;
    private String ShelfAreaID;

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getProductId() {
        return ProductId;
    }

    public void setProductId(String productId) {
        ProductId = productId;
    }

    public double getNumber() {
        return Number;
    }

    public void setNumber(double number) {
        Number = number;
    }

    public int getIsCheckRight() {
        return IsCheckRight;
    }

    public void setIsCheckRight(int isCheckRight) {
        IsCheckRight = isCheckRight;
    }

    public String getShelfAreaId() {
        return ShelfAreaID;
    }

    public void setShelfAreaId(String shelfAreaId) {
        this.ShelfAreaID = shelfAreaId;
    }
}
